# Creation of Lists

List1 = []
print("Initial empty list: ", end='')
print(List1)

List2 = ['Hello World']
print("List with the use of string: ", end='')
print(List2)

List3 = ["Hello", "World"]
print("List with multiple values: ")
print(List3[0])
print(List3[1])

List4 = [['Hello', 'World'], ['HelloWorld']]
print("Multi-dimensional list: ", end='')
print(List4)

List5 = [1, 1, 2, 3, 5, 8, 13]
print("List with Fibonacci numbers: ", end='')
print(List5)

List6 = [1, 2, 'Hello', 3, 'World']
print("List with mixed values: ", end='')
print(List6)
