# Accessing elements from lists

List1 = ["Geeks", "for", "Geeks"]
print("Accessing two elements from list: ")
print(List1[0])
print(List1[2])

List2 = [['Geeks', 'For'], ['Geeks']]
print("Accessing elements from multi-dimensional list: ")
print(List2[0][1])
print(List2[1][0])

List3 = [1, 2, 'Geeks', 3, 'for', 4, 'World', 5]
print("Accessing elements using negative indexing: ")
print(List3[-1])
print(List3[-4])
