# Creation of string

String1 = 'Hello World'
print("String with the use of single quotes: ", end='')
print(String1)

String2 = "'Hello World' 2"
print("String with the use of double quotes: ", end='')
print(String2)

String3 = '''"Hello World! 3"'''
print("String with the use of triple quotes: ", end='')
print(String3)

String4 = '''Hello
World
4'''
print("Creating a multi-line string: ")
print(String4)
