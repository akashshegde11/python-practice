# Slicing of tuples

Tuple1 = tuple('GeeksForGeeks')

print("Removal of first element: ")
print(Tuple1[1:])

print("Tuple after sequence of elements is reversed: ")
print(Tuple1[::-1])

print("Printing elements between Range 4-8: ")
print(Tuple1[4:8])

